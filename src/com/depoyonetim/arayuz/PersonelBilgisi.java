    public void totalSales() {
        int sayi = employeeTable.getRowCount();
        double totalSale = 0;
        for (int i = 0; i < sayi; i++) {
            String totalSaleDouble = employeeModel.getValueAt(i, 6).toString();
            totalSale += veriIslemleri.priceReturnFormat(totalSaleDouble);
        }
        totalMoneyTextLabel.setText(veriIslemleri.priceFormat(totalSale) + " TL");
    }

    public void employeeRankings() {
        int rowCount = employeeTable.getRowCount();
        String bestEmployeeString = employeeModel.getValueAt(0, 6).toString();
        double bestEmployee = veriIslemleri.priceReturnFormat(bestEmployeeString) ;
        String bestEmployeeS = employeeModel.getValueAt(0, 1).toString() + " " + employeeModel.getValueAt(0, 2).toString();
        String badEmployeeString = employeeModel.getValueAt(0, 6).toString();
        double badEmployee = veriIslemleri.priceReturnFormat(badEmployeeString);
        String badEmployeeS = employeeModel.getValueAt(0, 1).toString() + " " + employeeModel.getValueAt(0, 2).toString();
        for (int i = 0; i < rowCount; i++) {
            String versusString = employeeModel.getValueAt(i, 6).toString();
            double versus = veriIslemleri.priceReturnFormat(versusString) ;
            String versusS = employeeModel.getValueAt(i, 1).toString() + " " + employeeModel.getValueAt(i, 2).toString();
            if (versus > bestEmployee) {
                bestEmployee = versus;
                bestEmployeeS = versusS;
            }
            if (versus < badEmployee) {
                badEmployee = versus;
                badEmployeeS = versusS;
            }
        }
        bestEmployeeMoneyTextLabel.setText( new DecimalFormat("###,###.##").format(bestEmployee) + " TL");
        bestEmployeeTextLabel.setText(bestEmployeeS);
        badEmployeeTextLabel.setText(badEmployeeS);
        badEmployeeMoneyTextLabel.setText(new DecimalFormat("###,###.##").format(badEmployee) + " TL");
    }

    public void updateEmpoloyeeInformationTable() {
        String sorgu = "SELECT * FROM users";
        ResultSet updateRs = veriTabaniBaglantisi.baglanti(sorgu);
        employeeModel.setColumnIdentifiers(columns);
        try {
            while (updateRs.next()) {
                rows[0] = updateRs.getString("Id");
                rows[1] = updateRs.getString("firstName");
                rows[2] = updateRs.getString("lastName");
                rows[3] = updateRs.getString("userName");
                rows[4] = updateRs.getString("password");
                rows[5] = updateRs.getString("authorization");
                double totalSale = veriIslemleri.totalSales(updateRs.getString("userName"));
                rows[6] = veriIslemleri.priceFormat(totalSale);
                rows[7] = updateRs.getString("date");

                employeeModel.addRow(rows);
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
        employeeTable.setModel(employeeModel);
    }

