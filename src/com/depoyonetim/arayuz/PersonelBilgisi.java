    private void dateControlActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dateControlActionPerformed
        employeeModel.setRowCount(0);
        int firstDate = Integer.valueOf(firsDateYear.getSelectedItem().toString() + firstDateMonth.getSelectedItem().toString() + firstDateDay.getSelectedItem().toString());
        int secondDate = Integer.valueOf(secondDateYear.getSelectedItem().toString() + secondDateMonth.getSelectedItem().toString() + secondDateDay.getSelectedItem().toString());
        if (secondDate < firstDate) {
            errorMessage.setText("Lütfen Başlangıç Tarihi Bitiş Tarihinden Küçük Seçin");
            jLabel8.setText("");
            return;
        } else {
            jLabel8.setText(firstDateDay.getSelectedItem().toString() + "/" + firstDateMonth.getSelectedItem().toString() + "/" + firsDateYear.getSelectedItem().toString()
                    + " İle " + secondDateDay.getSelectedItem().toString() + "/" + secondDateMonth.getSelectedItem().toString() + "/" + secondDateYear.getSelectedItem().toString() + " Arası Bilgiler Gösteriliyor");
            String sorgu = "SELECT Id, userName,password, firstName, lastName, authorization, date FROM users" ;
            errorMessage.setText("");
            
            
            ResultSet dateRs = veriTabaniBaglantisi.baglanti(sorgu);
            employeeModel.setColumnIdentifiers(columns);
            try {
                while (dateRs.next()){
                    dateSearchRows[0] = dateRs.getString("Id");
                    dateSearchRows[1] = dateRs.getString("firstName");
                    dateSearchRows[2] = dateRs.getString("lastName");
                    dateSearchRows[3] = dateRs.getString("userName");
                    dateSearchRows[4] = dateRs.getString("password");
                    dateSearchRows[5] = dateRs.getString("authorization");
                    dateSearchRows[6] = veriIslemleri.dateSearchTotalSales(dateRs.getString("userName").toString(), firstDate, secondDate);
                    dateSearchRows[7] = dateRs.getString("date");
                    
                    employeeModel.addRow(dateSearchRows);
                }
            } catch (SQLException ex) {
                ex.printStackTrace();
                System.out.println("hata burada 1");
            }
            employeeTable.setModel(employeeModel);
        }
        employeeRankings();
        totalSales();

    }//GEN-LAST:event_dateControlActionPerformed

